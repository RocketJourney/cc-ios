import UIKit
import SwiftHEXColors

extension UIColor {
  class func rocketYellow()->UIColor {
    return UIColor(hex: 0xffcc00)!
  }
  class func rocketRed()->UIColor {
    return UIColor(hex: 0xff2517)!
  }
  class func rocketAlmostBlack()->UIColor {
    return UIColor(hex: 0x1a1a1a)!
  }
  class func rocketOrange()->UIColor {
    return UIColor(hex: 0xf794d1)!
  }
  class func rocketBlack()->UIColor {
    return UIColor(hex: 0x434343)!
  }
  class func rocketDarkGray()->UIColor {
    return UIColor(hex:0x202020)!
  }
  class func rocketGray()->UIColor {
    return UIColor(hex: 0x7f7f7f)!
  }
  class func rocketLightGray()->UIColor {
    return UIColor(hex: 0xe5e5e5)!
  }
  
  class func rocketGrayChat() -> UIColor {
    return UIColor(hex: 0xa3a3a3)!
  }
  class func rocketBlue()->UIColor {
    return UIColor(hex: 0x2e92ff)!
  }
}
